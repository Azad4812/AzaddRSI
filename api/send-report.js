export default async function handler(req, res) {
  if (req.method !== "POST") {
    return res.status(405).json({ error: "Method not allowed" });
  }

  const { entries, stats, winner, slackWebhook } = req.body;

  try {
    const today = new Date().toLocaleDateString("en-IN", {
      weekday: "long", day: "2-digit", month: "2-digit", year: "numeric",
      timeZone: "Asia/Kolkata"
    });

    const formatMins = (mins) => {
      if (mins === null || mins === undefined) return "—";
      if (mins < 60) return `${mins}m`;
      return `${Math.floor(mins / 60)}h ${mins % 60}m`;
    };

    const total = entries.length;
    const resolved = entries.filter(e => e.status === "Resolved").length;
    const pending = entries.filter(e => e.status === "Pending").length;
    const escalated = entries.filter(e => e.status === "Escalated").length;

    // Top issue
    const issueCount = {};
    entries.forEach(e => { issueCount[e.issue] = (issueCount[e.issue] || 0) + 1; });
    const topIssue = Object.entries(issueCount).sort((a, b) => b[1] - a[1])[0];

    // Fastest & slowest
    const allDiffs = entries.filter(e => e.diffMins).sort((a, b) => a.diffMins - b.diffMins);
    const fastest = allDiffs[0];
    const slowest = allDiffs[allDiffs.length - 1];

    // Build report
    const lines = [
      `📦 *EOD Performance Report — ${today}*`,
      ``,
      `📊 *Today's Summary*`,
      `• Total Issues: *${total}*  |  ✅ Resolved: *${resolved}*  |  ⏳ Pending: *${pending}*${escalated > 0 ? `  |  🔴 Escalated: *${escalated}*` : ""}`,
      ``,
      `👥 *Team Breakdown*`,
      ...stats.map(s =>
        `• *${s.person}* → ${s.total} issues | ${s.resolved} resolved | avg ${formatMins(s.avg)} | fastest ${formatMins(s.fastest)}${s.person === winner ? "  ⭐ *STAR OF THE DAY*" : ""}`
      ),
      ``,
      topIssue ? `🔥 *Top Issue Today:* ${topIssue[0]} (${topIssue[1]} cases)` : "",
      fastest ? `⚡ *Fastest Response:* ${fastest.respondedBy} — ${formatMins(fastest.diffMins)} (${fastest.sellerName})` : "",
      slowest && slowest.id !== fastest?.id ? `🐢 *Slowest Response:* ${slowest.respondedBy} — ${formatMins(slowest.diffMins)} (${slowest.sellerName})` : "",
      ``,
      `_Report auto-generated by Seller Response Tracker_`
    ].filter(l => l !== undefined);

    const reportText = lines.join("\n");

    // Post to Slack
    const slackRes = await fetch(slackWebhook, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ text: reportText })
    });

    if (!slackRes.ok) {
      return res.status(500).json({ error: "Slack webhook failed", status: slackRes.status });
    }

    return res.status(200).json({ success: true, report: reportText });

  } catch (err) {
    return res.status(500).json({ error: err.message });
  }
}
